#include <stdio.h>

typedef struct {
    unsigned char r, g, b, a;
} Couleur;

int main() {
    Couleur c[10] = {
        {0xef, 0x78, 0x12, 0xff}, {0x2c, 0xc8, 0x64, 0xff},
        {0x00, 0x99, 0xff, 0x80}, {0xff, 0x00, 0x00, 0xff},
        {0x00, 0xff, 0x00, 0xff}, {0x00, 0x00, 0xff, 0xff},
        {0x88, 0x88, 0x88, 0xaa}, {0xff, 0xff, 0x00, 0xcc},
        {0x12, 0x34, 0x56, 0xee}, {0xaa, 0xbb, 0xcc, 0xdd}
    };

    for (int i = 0; i < 10; i++) {
        printf("Couleur %d : R=%d G=%d B=%d A=%d\n", i + 1, c[i].r, c[i].g, c[i].b, c[i].a);
    }

    return 0;
}
